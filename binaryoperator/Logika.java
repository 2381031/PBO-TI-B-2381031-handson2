package binaryoperator;

public class Logika {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        boolean hasilAND = a && b;
        System.out.println("a AND b = " + hasilAND);

        boolean hasilor = a && b;
        System.out.println("a AND b = " + hasilor);

    }
}
